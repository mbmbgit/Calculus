"""
### 事業構造のピボット（転換）戦略

現状の「低単価・労働集約型（Linear Growth）」から「高付加価値・複利型（Exponential Growth）」へモデルを移行させる。

#### 1. 微分的改善（変化の加速）
- 毎月の平均単価を約 5,800円 ずつ向上させる施策（スキルアップ、アップセル）を実行する。
- 手数料10%の削減に向けた直販比率の向上。

#### 2. 積分的視点（資産の蓄積）
- 労働以外の収益源（テンプレート販売、ツール開発、外注チーム化）を構築し、10年後の面積（総収益）の底上げを図る。

"""

# 短期目標：2年以内に月商100万、単価20万にするためのシミュレーション
current_price = 61405
target_price = 200000
years = 2

# 1ヶ月ごとに必要な「単価の向上額（微分）」
monthly_increase = (target_price - current_price) / (years * 12)

print(f"【短期ロードマップ】")
print(f"来月からの目標単価アップ額: +{monthly_increase:.0f} 円 / 毎月")
print(f"24ヶ月後には単価 {target_price/10000:.0f} 万円を目指す。")
